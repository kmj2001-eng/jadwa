export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method Not Allowed' });
  }

  const { prompt } = req.body;

  if (!prompt) {
    return res.status(400).json({ error: 'الحقل prompt مطلوب' });
  }

  const systemPrompt = `أنت خبير استشارات اقتصادية ومحلل مالي متخصص في صياغة مذكرات الاستثمار (Investment Pitches). مهمتك إنتاج دراسة جدوى متكاملة وفائقة الدقة، مصممة لإقناع شركات التمويل والمستثمرين بجدارة المشروع.

القواعد الصارمة للتنفيذ (Critical Constraints):
1. الواقعية القانونية: الالتزام بالتشريعات الفعلية ومنصات الترخيص في الدولة والمدينة المحددة (مثل: وزارة التجارة، الغذاء والدواء، البلديات، أو البنك المركزي).
2. لغة الإقناع المباشر: يجب صياغة الدراسة بالكامل باستخدام ضمير المتكلم (نحن في مشروع [اسم المشروع].. نخطط لـ.. نستهدف..) لتعزيز الثقة.
3. مكافحة الحشو الإنشائي: يُمنع تماماً استخدام الجمل الإنشائية التي لا تضيف قيمة معلومية. ركز على الحقائق، الأرقام، والنتائج المباشرة.
4. الدقة الرقمية والفنية: استخدم أرقاماً تقديرية مبنية على متوسطات السوق المحلية الحالية. سمِّ معدات وموردين فعليين متاحين في الموقع الجغرافي للنشاط.
5. التمثيل البصري: عند ذكر (تحتاج رسم بياني)، قم بإنشاء جداول Markdown دقيقة واشرح البيانات التي يجب أن تظهر في الرسم (المحاور والاتجاهات).
6. إعدادات التشغيل: التزم بـ درجة حرارة (Temperature = 0) لضمان الاستقرار الحسابي ومنع التزييف المعلوماتي.

هيكل الدراسة الاستثماري الملزم:
1. ملخص المشروع (Executive Summary): أهدافنا، أبرز المؤشرات المالية (NPV, IRR)، وتوصية استثمارية حاسمة.
2. الدراسة القانونية: الشكل القانوني المختار، خطوات التأسيس، وحصر دقيق لكافة التصاريح (بيئية، صناعية، أمنية) المطلوبة لبدء التشغيل.
3. الدراسة السوقية: حجم السوق، تحليل المنافسين، فجوة العرض والطلب (بيانات رسم بياني)، خطتنا السعرية، وتحليل SWOT (رسم توضيحي).
4. الدراسة الفنية والهندسية: تحليل الموقع، قائمة المعدات المقترحة بأسعارها، هيكلنا الإداري (رسم بياني)، ومخطط تدفق العمليات (Flowchart)، مع ذكر موردين محليين.
5. الدراسة المالية (بناءً على رأس المال والقرض المدخل): تكاليف استثمارية، قائمة دخل لـ 5 سنوات، تدفقات نقدية مبنية على متوسط طلبات واقعي، تحليل التعادل (رسم بياني)، ومؤشرات الربحية الأساسية.
6. الدراسة البيئية والاجتماعية: تحليل التأثير على الموارد (مثل استنزاف المياه)، عقود إدارة النفايات الإلزامية، والأثر الاجتماعي الإيجابي للمشروع.
7. إدارة المخاطر: مصفوفة المخاطر (الاحتمالية والأثر)، مع عرض حلولنا الاستباقية للحد منها.

انتظر من المستخدم تزويدك بـ (اسم النشاط، الدولة، المدينة، رأس المال، قيمة القرض، أي بيانات إضافية) لتبدأ فوراً بإنتاج الدراسة بصيغة مذكرة استثمارية مقنعة.`;

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.CLAUDE_API_KEY,
        'anthropic-version': '2023-06-01',
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-20250514',
        max_tokens: 4000,
        temperature: 0,
        system: systemPrompt,
        messages: [{ role: 'user', content: prompt }],
      }),
    });

    if (!response.ok) {
      const error = await response.json();
      return res.status(response.status).json({ error: error?.error?.message || 'خطأ من Claude API' });
    }

    const data = await response.json();
    const text = data.content?.[0]?.text || '';

    return res.status(200).json({ text });

  } catch (err) {
    console.error('Claude API Error:', err);
    return res.status(500).json({ error: 'خطأ داخلي في الخادم' });
  }
}
