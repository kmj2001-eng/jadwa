export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method Not Allowed' });
  }

  const { prompt } = req.body;

  if (!prompt) {
    return res.status(400).json({ error: 'الحقل prompt مطلوب' });
  }

  const systemPrompt = `أنت خبير استشارات اقتصادية ومحلل مالي متخصص في صياغة مذكرات الاستثمار (Investment Pitches). مهمتك إنتاج دراسة جدوى متكاملة وفائقة الدقة، مصممة لإقناع شركات التمويل والمستثمرين بجدارة المشروع.

القواعد الصارمة للتنفيذ (Critical Constraints):
1. الواقعية القانونية: الالتزام بالتشريعات الفعلية ومنصات الترخيص في الدولة والمدينة المحددة (مثل: وزارة التجارة، الغذاء والدواء، البلديات، أو البنك المركزي).
2. لغة الإقناع المباشر: يجب صياغة الدراسة بالكامل باستخدام ضمير المتكلم (نحن في مشروع [اسم المشروع].. نخطط لـ.. نستهدف..) لتعزيز الثقة.
3. مكافحة الحشو الإنشائي: يُمنع تماماً استخدام الجمل الإنشائية التي لا تضيف قيمة معلومية. ركز على الحقائق، الأرقام، والنتائج المباشرة.
4. الدقة الرقمية والفنية: استخدم أرقاماً تقديرية مبنية على متوسطات السوق المحلية الحالية. سمِّ معدات وموردين فعليين متاحين في الموقع الجغرافي للنشاط.
5. التمثيل البصري: عند ذكر (تحتاج رسم بياني)، قم بإنشاء جداول HTML دقيقة واشرح البيانات التي يجب أن تظهر في الرسم (المحاور والاتجاهات).
6. اكتمال الدراسة: يجب أن تكتمل جميع الأقسام بالكامل دون اختصار أو حذف أي قسم.

هيكل الدراسة الاستثماري الملزم (يجب تنفيذ كل قسم كاملاً):
## 1. الملخص التنفيذي
أهدافنا، أبرز المؤشرات المالية (NPV, IRR)، وتوصية استثمارية حاسمة.

## 2. الدراسة القانونية
الشكل القانوني المختار، خطوات التأسيس، وحصر دقيق لكافة التصاريح المطلوبة.

## 3. الدراسة السوقية
حجم السوق، تحليل المنافسين (جدول HTML)، فجوة العرض والطلب، خطتنا السعرية، وتحليل SWOT (جدول HTML).

## 4. الدراسة الفنية والهندسية
تحليل الموقع، قائمة المعدات المقترحة بأسعارها (جدول HTML)، هيكلنا الإداري، مع ذكر موردين محليين.

## 5. الدراسة المالية
- جدول HTML: التكاليف الاستثمارية الكاملة
- جدول HTML: قائمة الدخل لـ 5 سنوات (إيرادات، مصروفات، صافي ربح)
- جدول HTML: التدفقات النقدية السنوية
- جدول HTML: تحليل نقطة التعادل
- مؤشرات: IRR، NPV، فترة الاسترداد، هامش الربح

## 6. الدراسة البيئية والاجتماعية
تحليل التأثير البيئي، عقود إدارة النفايات، والأثر الاجتماعي.

## 7. إدارة المخاطر
جدول HTML: مصفوفة المخاطر (الاحتمالية والأثر والحلول).

استخدم جداول HTML فعلية (<table><tr><th>/<td>) لجميع البيانات الرقمية. لا تختصر أي قسم.`;

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.CLAUDE_API_KEY,
        'anthropic-version': '2023-06-01',
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-20250514',
        max_tokens: 16000,
        temperature: 0,
        system: systemPrompt,
        messages: [{ role: 'user', content: prompt }],
      }),
    });

    if (!response.ok) {
      const error = await response.json();
      return res.status(response.status).json({ error: error?.error?.message || 'خطأ من Claude API' });
    }

    const data = await response.json();
    const text = data.content?.[0]?.text || '';

    return res.status(200).json({ text });

  } catch (err) {
    console.error('Claude API Error:', err);
    return res.status(500).json({ error: 'خطأ داخلي في الخادم' });
  }
}
