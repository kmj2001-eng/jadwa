// ─── إزالة التكرار (نفس claude.js) ──────────────────────────────
function removeOverlap(existing, continuation) {
  const maxCheck = Math.min(existing.length, 1000);
  const tail = existing.slice(-maxCheck);
  for (let len = Math.min(continuation.length, maxCheck); len > 60; len--) {
    if (tail.endsWith(continuation.slice(0, len))) return continuation.slice(len);
  }
  return continuation;
}

// ─── استدعاء Anthropic API (مع دعم max_tokens continuation) ─────
async function generateSection(apiKey, model, formData, sectionNum) {
  const userMessage = buildUserMessage(formData, sectionNum);

  const callAPI = async (messages, maxTok) => {
    const res = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': apiKey,
        'anthropic-version': '2023-06-01',
      },
      body: JSON.stringify({
        model,
        max_tokens: maxTok,
        temperature: 0,
        stream: false,
        system: ROLE,
        messages,
      }),
    });
    if (!res.ok) {
      const err = await res.json().catch(() => ({}));
      throw new Error(`HTTP ${res.status}: ${err?.error?.message || 'Unknown'}`);
    }
    return res.json();
  };

  // الاستدعاء الأول
  const r1 = await callAPI([{ role: 'user', content: userMessage }], 16000);
  let fullText = r1.content?.[0]?.text || '';
  let stopReason = r1.stop_reason;

  // متابعة إن اقتُطع
  for (let pass = 0; pass < 2 && stopReason === 'max_tokens'; pass++) {
    const tail = fullText.slice(-300).trim();
    const r2 = await callAPI([
      { role: 'user',      content: userMessage },
      { role: 'assistant', content: fullText },
      { role: 'user',      content: `أكمل النص من النقطة التي توقفت عندها بالضبط. آخر ما كتبته:\n"...${tail}"\nاستمر مباشرةً بعد هذه النقطة دون أي تكرار.` },
    ], 8000);
    const cont = r2.content?.[0]?.text || '';
    fullText += removeOverlap(fullText, cont);
    stopReason = r2.stop_reason;
  }

  return fullText;
}

// ─── الدالة الرئيسية ──────────────────────────────────────────────
async function main() {
  const apiKey = process.env.CLAUDE_API_KEY || process.env.CLAUDE_KEY || process.env.claude_key;
  if (!apiKey) {
    console.error('❌ الرجاء ضبط متغير البيئة CLAUDE_API_KEY');
    process.exit(1);
  }

  const model = process.env.CLAUDE_MODEL || 'claude-sonnet-4-20250514';
  console.log(`\n🚀 بدء توليد 3 دراسات جدوى بنموذج: ${model}\n`);

  const output = {};

  for (const study of STUDIES) {
    console.log(`\n📂 الدراسة ${study.id}: ${study.title}`);
    const sectionTexts = [];

    for (let sec = 1; sec <= 10; sec++) {
      const label = [
        'الملخص التنفيذي','الإطار الاستراتيجي','الدراسة السوقية',
        'الدراسة الفنية','الدراسة القانونية','الدراسة المالية',
        'إدارة المخاطر','خطة التنفيذ','الأثر البيئي','الخلاصة والتوصيات',
      ][sec - 1];

      process.stdout.write(`  ⏳ القسم ${sec}/10 — ${label}...`);
      const start = Date.now();

      let text = '';
      let attempts = 0;
      while (attempts < 3) {
        try {
          text = await generateSection(apiKey, model, study.formData, sec);
          break;
        } catch (e) {
          attempts++;
          console.error(`\n  ⚠️ محاولة ${attempts}: ${e.message}`);
          if (attempts < 3) await new Promise(r => setTimeout(r, 3000));
        }
      }

      const secs = ((Date.now() - start) / 1000).toFixed(1);
      const words = text.trim().split(/\s+/).length;
      console.log(` ✅ ${words} كلمة في ${secs}ث`);
      sectionTexts.push(text);
    }

    output[study.id] = {
      title:    study.title,
      formData: study.formData,
      text:     sectionTexts.filter(t => t.trim()).join('\n\n'),
      generatedAt: new Date().toISOString(),
    };

    console.log(`  ✅ الدراسة ${study.id} اكتملت`);
  }

  // حفظ النتيجة
  const dataDir = path.join(__dirname, '..', 'data');
  if (!fs.existsSync(dataDir)) fs.mkdirSync(dataDir, { recursive: true });
  fs.writeFileSync(OUT_FILE, JSON.stringify(output, null, 2), 'utf8');

  const totalWords = Object.values(output).reduce((sum, s) => sum + s.text.split(/\s+/).length, 0);
  console.log(`\n✅ تم الحفظ في: ${OUT_FILE}`);
  console.log(`📊 إجمالي الكلمات: ${totalWords.toLocaleString('ar-EG')}\n`);
}

main().catch(e => { console.error('❌ خطأ:', e.message); process.exit(1); });
